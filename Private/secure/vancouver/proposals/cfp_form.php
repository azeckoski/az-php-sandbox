			<table ><tbody><tr><td valign="top"><strong>Management &amp; Campus Implementation</strong><br />
This track highlights issues of concern to those managing current or upcoming pilot implementations; campus impact and implications; project assessment implications; training; coordination and leadership; and institutional change. This is appropriate for implementations of Sakai, OSP, or any interest regarding other strategies for integration of Sakai tools.</td></tr><tr><td valign="top"><strong>
Research and Collaboration</strong><br />

The Research and Collaboration track will focus on using the Sakai Collaboration and Learning Environment (CLE) and its tools such as OSP or Melete, can be used to support  research collaborations. We are looking for existing examples, current or upcoming projects, uses cases, and plans.</td></tr><tr><td valign="top"><strong>
Technology and User Interface</strong><br />
The Technology and User Interface track will offer sessions, panels, and round-tables that cover features and requirements; technical challenges and lessons learned for installing and implementing Sakai and integrated tools such as OSP, Samigo, and Melete; contributing code; tech support; commercial tech support; future releases; usability, user-interface design, and user-testing.
</td></tr><tr><td valign="top"><strong>
Teaching, Learning, and Assessment </strong><br />
The Teaching, Learning, and Assessment track will offer opportunuties to share experiences with and plans for using Sakai or OSP for teaching and learning. These sessions will include presentations on the impact of OSP (or portfolios in general) on teaching practices; discussions regarding the value of a Collaborative Learning Environment (CLE) and its integrated toolset for students and the learning process; assessment management systems for tracking student progress toward learning goals; online learning using Sakai/OSP; and supporting reflective practices.
</td></tr><tr><td valign="top"><strong>Sakai Foundation, Community Source &amp; Governance</strong><br />
These sessions will focus on the transition from the current organization to the Sakai Foundation as the operational, governance, and legal framework for Sakai and OSPI. Issues and process around open and community source licensing and copyright will also be covered here.</td></tr></tbody></table>			</td>
