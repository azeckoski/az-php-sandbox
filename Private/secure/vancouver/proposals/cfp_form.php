			<p><strong>Preparation for proposal submission:
</strong></p>
<ul>
<li><strong>Deadlines?</strong></li>
<ul>
<li>Proposals must be submitted by:&nbsp;&nbsp; <strong>March 3, 2006.</strong></li>

<li>You will be notified by March 31, 2006&nbsp; if your proposal has been accepted.</li> </ul></ul>
<ul>
<li><strong>Who can present?</strong>&nbsp; - Anybody within the community is welcome to present.<br>
<br>
</li>
<li>The <strong>title</strong> and <strong>abstract</strong> (50 words, max.) from your proposal form will be used,  perhaps
verbatim -- in the conference
program. Therefore, make sure they
accurately reflect the content and purpose of your session. Keep in
mind that your audience will consist of current and potential Sakai
Educational Partner Program members, the Open Source Portfolio members, and attendees new to both projects.<br>

<br>
</li>
<li>
The <strong>description</strong>
(150-words, max.) of your session should be addressed to the program committee for their
review of your proposal. Therefore, in your session description, please describe specifically what technology, learning
situation, research process, implementation problem, assessment
program, etc. that you plan on talking about. Secondly, explain what
your audience will learn from your session.&nbsp;<br>
<br>
</li>
<li><strong>Tell us </strong>in what
way your topic is unique, or valuable, or proven, or timely -  why
should we choose your proposal for the program?</li></ul>We will also ask you to think carefully about your&nbsp;<strong>audience</strong>,  developers, faculty, tech support, administrators,
researchers -- and your <strong>goals</strong> for the session. Depending on your
audience, session proposals should address not only <strong>HOW,</strong> but also <strong>WHY</strong>

and <strong>WHEN</strong>. The more practical, focused, informative, and action-oriented
your session, the more valuable it will be for this conference. Each
session should use visuals, and provide handouts when appropriate.
Allow ample time for questions. Each session will have a convener who
will make sure your session runs well and ends on time. <br>			</td>
