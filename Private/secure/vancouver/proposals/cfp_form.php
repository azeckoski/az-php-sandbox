			<td valign="top" colspan="2">
			<p>For the Sakai project, this is a year of&nbsp; community building,
implementations (and all that implies), and exploration of the
frontiers of innovation for the Sakai Collaboration and Learning
Environment. Session presenters should incorporate these themes into their presentations where
ever
possible.&nbsp;</p>
<p>Before submitting a proposal, we strongly encourage you to download
a sample submission form (available soon ) which will help you prepare
your text&nbsp; prior to using the online submission form.&nbsp;&nbsp;</p>


<ul><strong>Presentation
formats include:</strong>
<li><strong>Panel Session</strong> - This type of session typically brings
together panelists from diverse background to address a topic from
multiple points of view. &nbsp;</li>
<li><strong>Workshop</strong> - This type of session is highly interactive, relaying skills as well as information to attendees.</li>

<li><strong>Discussion/Roundtable</strong>- This type of session involves a very
brief presentation of a topic and immediately opens for discussion of
the topic by attendees.</li>
<li><strong>Lecture/Presentation</strong>- This type of session consists mostly
of presenting information to the attendees. Sufficient time for
follow-up questions is included.</li>
<li><strong>Showcase/Poster session</strong>- Please note  that we will not be soliciting proposals for Showcase/Poster sessions using this call for proposals form.  We do, however, encourage you to create a poster  that showcases your campus implementation or toolset. You can use the template we will provide (sometime in March) or use your own design. We will provide more details in March.   </li></ul>

<p>Use the best format to achieve your goals and provide a
meaningful experience for your participants. The program committee may
suggest alternate formats for session based upon time and room
constraints and other related session proposals</p>
<p><strong>Technical Demos:</strong><br>
Is a Technical Demonstration a more appropriate option for you?&nbsp; The technical demo reception will
be held on the evening of Thursday, June 1st.&nbsp; As in the past, we plan
to provide each demonstrator with table space and, if equipment
resources allow, an overhead projector and screen to project your
demonstration.&nbsp;&nbsp; This event has become one of the most exciting events
of the conference.&nbsp; Space is very limited, so get your demo requests in
early.&nbsp; To submit a Technical Demo request, select the "Technical Demo"
option when completing the Call for Proposals form.</p>		<br /><br /><br />	</td>
